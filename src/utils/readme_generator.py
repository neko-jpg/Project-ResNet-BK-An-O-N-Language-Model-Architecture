## üß† Capabilities (Future IQ)
"""
        if skills:
            md += "| Skill | Score |\n|---|---|\n"
            for k, v in skills.items():
                md += f"| {k} | {v:.1f} |\n"
        else:
            md += "Skills evaluation not available.\n"

        md += """
## üöÄ Usage

